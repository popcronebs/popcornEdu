        function firstLoginStudyTimeSelectEnd(vthis){
            // 선택된 요일, 시, 분, 부터 경과시간(interval)을 가져와서 
            // bundle 의 row 를 복사해서 붙여넣는다.
            const sel_days = document.querySelector('[data-teach-info="sel_days"]').value;
            const start_time_hour = document.querySelector('[data-teach-info="start_time_hour"]').value;
            const start_time_min = document.querySelector('[data-teach-info="start_time_min"]').value;
            const study_time_interval = document.querySelector('[data-teach-info="study_time_interval"]').value;
            if(sel_days.length < 1 || start_time_hour.length < 1 || start_time_min.length < 1 || study_time_interval.length < 1){
                toast('수업시간을 선택해주세요.');
                document.querySelector('[data-teach-info="study_time_interval"]').value = 0;
                return;
            } 
            const select_index = document.querySelector('[data-teach-info="sel_days"]').options.selectedIndex;
            const sel_days_str = document.querySelector('[data-teach-info="sel_days"]').options[select_index].text;
            // 시작시간 시:분 에서 interval 분을 더해서 종료시간을 계산한다.
            let end_time = new Date();
            end_time.setHours(start_time_hour);
            end_time.setMinutes(start_time_min);
            end_time.setMinutes(end_time.getMinutes() + parseInt(study_time_interval));
            const end_time_str = `${end_time.getHours()}:${end_time.getMinutes().toString().padStart(2, '0')}`;

            // 10단위 밑 분은 앞에 0을 붙여준다.
            const study_time_str = `${sel_days_str} ${start_time_hour}:${start_time_min.padStart(2, '0')} - ${end_time_str} (${study_time_interval}분)`;
            const div_bundle = document.querySelector('[data-div-after-first-login-study-time-bundle]');
            const row_clone = document.querySelector('[data-div-after-first-login-study-time-row="copy"]').cloneNode(true);
            row_clone.setAttribute('data-div-after-first-login-study-time-row', 'clone');
            row_clone.hidden = false;
            row_clone.querySelector('[data-study-time-str]').innerText = study_time_str;
            row_clone.querySelector('[data-study-time-day]').value = sel_days;
            row_clone.querySelector('[data-study-time-start]').value = `${start_time_hour}:${start_time_min}`;
            row_clone.querySelector('[data-study-time-end]').value = end_time_str;
            row_clone.querySelector('[data-study-time-interval]').value = study_time_interval;
            div_bundle.appendChild(row_clone);
            // 수업설정 초기화 / 다시 선택을 해주기 위해서.
            firstLoginStudyTimeReset();
        }
